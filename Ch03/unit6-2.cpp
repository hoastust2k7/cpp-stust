#include <stdio.h>
#include <stdlib.h>

int main(int argc, char *argv[]) {
    int x = 5;
    printf("----------------------------------------------------------------------------------\n");
    printf("|  運算子  |        功能        | 實例（假如每列 x 初值為 5） |       結果       |\n");
    printf("----------------------------------------------------------------------------------\n");
    printf("|     =    |        指定        |            x = 5            |     x <-- %d      |\n", x, x);
    printf("----------------------------------------------------------------------------------\n");
    printf("|    +=    |    相加後再指定    |   x += 4 相當於 x = x + 4   | x <-- %d + 4 = %d  |\n", x,  x + 4);
    printf("----------------------------------------------------------------------------------\n");
    printf("|    -=    |    相減後再指定    |   x -= 2 相當於 x = x - 2   | x <-- %d - 2 = %d  |\n", x,  x - 2);
    printf("----------------------------------------------------------------------------------\n");
    printf("|    *=    |    相乘後再指定    |   x *= 3 相當於 x = x * 3   | x <-- %d * 3 = %d |\n", x, x * 3);
    printf("----------------------------------------------------------------------------------\n");
    printf("|    /=    |    相除後再指定    |   x *= 2 相當於 x = x * 2   | x <-- %d / 2 = %d  |\n", x, x / 2);
    printf("----------------------------------------------------------------------------------\n");
    printf("|    %%=    | 相除曲餘數後再指定 |   x %%= 2 相當於 x = x %% 2   | x <-- %d %% 2 = %d  |\n", x, x % 2);
    printf("----------------------------------------------------------------------------------\n");
    system("PAUSE");
    return 0;
}